// Math

// Math.max is used to find the largest number in an array

const arr = [10,324,45,90,9808]
console.log("Largest number in given array is",Math.max(10,324,45,90,9808));
// Output : Largest number in given array is 9808

const a = [1, 2, 3, 4, 5];
console.log("Largest number in given array is",Math.max(1, 2, 3, 4, 5));
// Output : Largest number in given array is 5

const b = [10, 324, 45, 90, 9999];
